public class CountingJava { public static void main(String[] args) {
int X = 0; System.out.println( " X = " + X );
X = X + 1; System.out.println( " X = " + X );
X = X + 1; System.out.println( " X = " + X );
X = X + 1; System.out.println( " X = " + X );
while ( X != 0) {
X = X - 1; System.out.println( " X = " + X );
}
} }
